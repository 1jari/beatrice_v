/* +----------------+ */
/* | Signed Vectors | */
/* +----------------+ */
pub struct vec2i16_t {
    pub x:  i16,
    pub y:  i16,
}
pub struct vec2i32_t {
    pub x:  i32,
    pub y:  i32,
}
pub struct vec2i64_t {
    pub x:  i64,
    pub y:  i64,
}

/* +------------------+ */
/* | Unsigned Vectors | */
/* +------------------+ */
pub struct vec2u16_t {
    pub x:  u16,
    pub y:  u16,
}
pub struct vec2u32_t {
    pub x:  u32,
    pub y:  u32,
}
pub struct vec2u64_t {
    pub x:  u64,
    pub y:  u64,
}

/* +------------------+ */
/* | Floating Vectors | */
/* +------------------+ */
pub struct vec2f32_t {
    pub x:  f32,
    pub y:  f32,
}
pub struct vec2f64_t {
    pub x:  f64,
    pub y:  f64,
}